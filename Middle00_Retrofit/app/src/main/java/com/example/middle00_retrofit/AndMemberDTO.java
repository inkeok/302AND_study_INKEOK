package com.example.middle00_retrofit;

public class AndMemberDTO {
	private String email, pw;

	
	
	public AndMemberDTO(String email, String pw) {
		super();
		this.email = email;
		this.pw = pw;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	public String getPw() {
		return pw;
	}

	public void setPw(String pw) {
		this.pw = pw;
	}
	
	

	
	
	
}
